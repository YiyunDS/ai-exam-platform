"use client"

import * as React from "react"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const sidebarVariants = cva(
  "flex h-full w-full flex-col bg-sidebar text-sidebar-foreground",
  {
    variants: {
      variant: {
        sidebar: "border-r border-sidebar-border",
        floating: "border border-sidebar-border",
        inset: "border-r border-sidebar-border bg-sidebar-accent",
      },
      side: {
        left: "",
        right: "",
      },
    },
    defaultVariants: {
      variant: "sidebar",
      side: "left",
    },
  }
)

const Sidebar = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & VariantProps<typeof sidebarVariants>
>(({ className, variant, side, ...props }, ref) => {
  return (
    <div
      ref={ref}
      data-sidebar="sidebar"
      data-side={side}
      className={cn(sidebarVariants({ variant, side }), className)}
      {...props}
    />
  )
})
Sidebar.displayName = "Sidebar"

